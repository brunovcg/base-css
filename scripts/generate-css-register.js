import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { pathToFileURL } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const baseDesignDir = path.resolve(__dirname, "../src/styles/design");
const outputFile = path.resolve(__dirname, "../src/css/css.register.ts");

const toCamelCase = (str) => {
  return str.replace(/-([a-z])/g, (g) => g[1].toUpperCase());
};

const scanDesignDir = (dir, register) => {
  if (!fs.existsSync(dir)) {
    console.warn(`Design directory not found: ${dir}`);
    return;
  }

  const files = fs.readdirSync(dir).filter((file) => file.endsWith(".css"));

  files.forEach((file) => {
    const filePath = path.join(dir, file);
    let content = fs.readFileSync(filePath, "utf-8");

    // Remove comments
    content = content.replace(/\/\*[\s\S]*?\*\//g, "");

    const name = file.replace(".css", "");
    const key = toCamelCase(name);

    const classRegex = /\.(-?[_a-zA-Z]+[_a-zA-Z0-9-]*)/g;

    const classes = new Set(register[key] || []);
    let match;
    while ((match = classRegex.exec(content)) !== null) {
      classes.add(match[1]);
    }

    register[key] = Array.from(classes).sort();
  });
};

const loadConfig = async () => {
  const cwd = process.cwd();
  const configPath = path.resolve(cwd, "base-css.config.js");

  if (fs.existsSync(configPath)) {
    try {
      const configUrl = pathToFileURL(configPath).href;
      const configModule = await import(configUrl);
      return configModule.default || configModule;
    } catch (error) {
      console.warn(`Failed to load config from ${configPath}:`, error.message);
    }
  }

  return null;
};

const main = async () => {
  const register = {};

  // 1. Scan base-css's own design directory
  scanDesignDir(baseDesignDir, register);

  // 2. Check for consumer config file
  const config = await loadConfig();

  if (config?.designDir) {
    const cwd = process.cwd();
    const extraDir = path.resolve(cwd, config.designDir);
    console.log(`Scanning consumer design directory: ${extraDir}`);
    scanDesignDir(extraDir, register);
  }

  const fileContent = `// Do not edit this file manually
// This file is auto-generated by scripts/generate-css-register.js

export const CSS_REGISTER = {
${Object.entries(register)
  .map(([key, classes]) => `  ${key}: ${JSON.stringify(classes).replace(/"/g, "'")},`)
  .join("\n")}
} as const satisfies Record<string, string[]>;

`;

  fs.writeFileSync(outputFile, fileContent);
  console.log(`Generated ${outputFile}`);
};

main();
